// ========== CONSOLE ERROR CATCHER ==========

function startConsoleMonitoring() {
    const originalConsole = {
        log: console.log,
        error: console.error,
        warn: console.warn,
        info: console.info,
        debug: console.debug
    };

    // Override console methods
    console.log = function(...args) {
        originalConsole.log.apply(console, args);
        addConsoleLog('info', args.join(' '));
    };

    console.error = function(...args) {
        originalConsole.error.apply(console, args);
        addConsoleLog('error', args.join(' '));
        diagnosticState.errorCount++;
        updateErrorCount();
    };

    console.warn = function(...args) {
        originalConsole.warn.apply(console, args);
        addConsoleLog('warning', args.join(' '));
    };

    console.info = function(...args) {
        originalConsole.info.apply(console, args);
        addConsoleLog('info', args.join(' '));
    };

    console.debug = function(...args) {
        originalConsole.debug.apply(console, args);
        addConsoleLog('info', `[DEBUG] ${args.join(' ')}`);
    };
}

function addConsoleLog(type, message) {
    const timestamp = new Date().toLocaleTimeString();
    const logEntry = document.createElement('div');
    logEntry.className = `log-entry log-${type}`;
    logEntry.textContent = `[${timestamp}] ${message}`;
    
    const logContainer = document.getElementById('console-log');
    if (logContainer) {
        logContainer.appendChild(logEntry);
        logContainer.scrollTop = logContainer.scrollHeight;
        
        diagnosticState.consoleLog.push({ type, message, timestamp });
        
        // Keep only last 100 entries
        if (diagnosticState.consoleLog.length > 100) {
            diagnosticState.consoleLog.shift();
            if (logContainer.children.length > 100) {
                logContainer.removeChild(logContainer.firstChild);
            }
        }
    }
}

function clearConsoleLog() {
    const logContainer = document.getElementById('console-log');
    if (logContainer) {
        logContainer.innerHTML = 
            '<div class="log-entry log-info">[Cleared] Console log cleared</div>';
        diagnosticState.consoleLog = [];
        addConsoleLog('info', 'Console log cleared');
    }
}

