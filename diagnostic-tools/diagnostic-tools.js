async function runHealthChecks() {
    const deploymentStatus = document.getElementById('deployment-status');
    if (deploymentStatus) {
        deploymentStatus.textContent = 'Checking...';
        deploymentStatus.className = 'status-badge status-warning';
    }
    
    const checks = [
        { id: 'backend-status', url: '/api/health', name: 'Backend API' },
        { id: 'database-status', url: '/api/health/db', name: 'Database' },
        { id: 'auth-status', url: '/api/auth/health', name: 'Authentication Service' }
    ];
    
    let allHealthy = true;
    
    for (const check of checks) {
        const element = document.getElementById(check.id);
        if (!element) continue;
        
        element.textContent = 'Checking...';
        element.style.color = '#f59e0b';
        
        try {
            // Simulate API check (in real implementation, this would be actual fetch)
            await new Promise(resolve => setTimeout(resolve, 300 + Math.random() * 700));
            
            // Simulate 85% success rate
            const isHealthy = Math.random() > 0.15;
            
            if (isHealthy) {
                element.textContent = '✅ Online';
                element.style.color = '#10b981';
                diagnosticState.healthCheckResults[check.name] = { status: 'healthy', timestamp: new Date() };
                addConsoleLog('success', `${check.name} health check passed`);
            } else {
                element.textContent = '❌ Offline';
                element.style.color = '#ef4444';
                diagnosticState.healthCheckResults[check.name] = { status: 'unhealthy', timestamp: new Date() };
                addConsoleLog('error', `${check.name} health check failed`);
                allHealthy = false;
            }
        } catch (error) {
            element.textContent = '❌ Error';
            element.style.color = '#ef4444';
            diagnosticState.healthCheckResults[check.name] = { status: 'error', timestamp: new Date(), error: error.message };
            addConsoleLog('error', `${check.name} health check error: ${error.message}`);
            allHealthy = false;
        }
    }
    
    // Update deployment status
    if (deploymentStatus) {
        if (allHealthy) {
            deploymentStatus.textContent = 'Healthy';
            deploymentStatus.className = 'status-badge status-active';
            addConsoleLog('success', 'All health checks passed');
        } else {
            deploymentStatus.textContent = 'Degraded';
            deploymentStatus.className = 'status-badge status-warning';
            addConsoleLog('warning', 'Some services are experiencing issues');
        }
    }
}

