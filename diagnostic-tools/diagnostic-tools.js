// ========== NETWORK REQUEST MONITOR ==========

function startNetworkMonitoring() {
    // Monitor fetch API
    const originalFetch = window.fetch;
    
    window.fetch = function(...args) {
        const startTime = Date.now();
        const requestId = diagnosticState.requestCount + 1;
        diagnosticState.requestCount++;
        
        const url = typeof args[0] === 'string' ? args[0] : args[0].url;
        const method = args[1]?.method || 'GET';
        
        addNetworkLog('info', `→ ${method} ${url}`);
        updateRequestCount();
        
        return originalFetch.apply(this, args)
            .then(response => {
                const duration = Date.now() - startTime;
                addNetworkLog('success', `← ${response.status} ${response.statusText} (${duration}ms)`);
                return response;
            })
            .catch(error => {
                const duration = Date.now() - startTime;
                diagnosticState.errorCount++;
                addNetworkLog('error', `✗ ${error.message} (${duration}ms)`);
                updateErrorCount();
                throw error;
            });
    };

    // Monitor XMLHttpRequest
    const originalXHROpen = XMLHttpRequest.prototype.open;
    const originalXHRSend = XMLHttpRequest.prototype.send;
    
    XMLHttpRequest.prototype.open = function(method, url) {
        this._requestMethod = method;
        this._requestUrl = url;
        this._startTime = Date.now();
        return originalXHROpen.apply(this, arguments);
    };
    
    XMLHttpRequest.prototype.send = function(body) {
        diagnosticState.requestCount++;
        
        addNetworkLog('info', `→ ${this._requestMethod} ${this._requestUrl}`);
        updateRequestCount();
        
        this.addEventListener('load', function() {
            const duration = Date.now() - this._startTime;
            addNetworkLog('success', `← ${this.status} (${duration}ms)`);
        });
        
        this.addEventListener('error', function() {
            const duration = Date.now() - this._startTime;
            diagnosticState.errorCount++;
            addNetworkLog('error', `✗ Request failed (${duration}ms)`);
            updateErrorCount();
        });
        
        return originalXHRSend.apply(this, arguments);
    };
}

