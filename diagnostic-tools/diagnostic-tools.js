// ========== SYSTEM INFORMATION ==========

function updateSystemInfo() {
    // Browser info
    const browserElement = document.getElementById('browser-info');
    if (browserElement) {
        browserElement.textContent = getBrowserInfo();
    }
    
    // Screen resolution
    const screenElement = document.getElementById('screen-res');
    if (screenElement) {
        screenElement.textContent = `${window.screen.width}×${window.screen.height}`;
    }
    
    // Connection type
    const connectionElement = document.getElementById('connection-type');
    if (connectionElement) {
        connectionElement.textContent = navigator.connection ? 
            navigator.connection.effectiveType || 'Unknown' : 'Unknown';
    }
    
    // Timezone
    const timezoneElement = document.getElementById('timezone');
    if (timezoneElement) {
        timezoneElement.textContent = Intl.DateTimeFormat().resolvedOptions().timeZone;
    }
}

function getBrowserInfo() {
    const ua = navigator.userAgent;
    let browser = "Unknown";
    
    if (ua.includes("Chrome") && !ua.includes("Edg")) {
        browser = "Chrome";
    } else if (ua.includes("Firefox")) {
        browser = "Firefox";
    } else if (ua.includes("Safari") && !ua.includes("Chrome")) {
        browser = "Safari";
    } else if (ua.includes("Edg")) {
        browser = "Edge";
    }
    
    return browser;
}

